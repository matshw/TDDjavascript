function adicao(v1, v2){
    return v1 + v2;
}

function subtracao(valor1, valor2){
    return valor1 - valor2;
}

function multiplicacao(valor1, valor2){
    return valor1 * valor2;
}

function divisao(valor1, valor2){
    return valor1 / valor2;
}

module.exports = {
    adicao: adicao,
    subtracao: subtracao,
    multiplicacao: multiplicacao,
    divisao: divisao
};
